    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        i--;
        Main.usuario = ManagerUsuario.autentificar(jTextField1.getText().toUpperCase(), jPasswordField1.getText().toUpperCase());
        if (Main.usuario != null) {
            ManagerArchivo.escribirLog("Sesion Iniciada Por el Usuario ->"+Main.usuario);
            Main.iniciarSecion();
            this.dispose();
        } else {
            if (i == 0) {
                JOptionPane.showMessageDialog(this, "Numero de Intentos superados", "Acceso Denegado", JOptionPane.ERROR_MESSAGE);
                ManagerArchivo.escribirLog("Cierre del Programa por Numero de Intentos Superados ->" + jTextField1.getText());
                System.exit(i);
            } else {
                JOptionPane.showMessageDialog(this, "Usuario o Contraseña no Validos\nLe quedan " + i + " Intentos", "Contraseña Equivocada", JOptionPane.ERROR_MESSAGE);
                ManagerArchivo.escribirLog("Intento Fallido para Iniciar Secion del usuario ->" + jTextField1.getText());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

