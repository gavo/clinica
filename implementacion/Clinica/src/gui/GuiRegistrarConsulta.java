    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Consulta c = new Consulta();
        c.setCosto(especialidades.get(jComboBox1.getSelectedIndex()).getCosto());
        c.setAtencion(SQL.formatDates.format(jDateChooser1.getDate()));
        c.setCi(p);
        c.setId_us_a(especialistas.get(jComboBox2.getSelectedIndex()));
        ManagerConsulta.registrarConsulta(c);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        if (jComboBox1.getItemCount() > 0) {
            cargarEspecialistas();
            jLabel4.setText("Costo : " + especialidades.get(jComboBox1.getSelectedIndex()).getCosto());
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

    private void jDateChooser1PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jDateChooser1PropertyChange
        if (jDateChooser1.getDate() != null) {
            jButton1.setEnabled(true);
            jLabel6.setText(ManagerConsulta.numeroFichaSiguiente(jDateChooser1.getDate()) + "");
        }
    }//GEN-LAST:event_jDateChooser1PropertyChange

    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        String ci = JOptionPane.showInputDialog("Ingrese el Numero de CI del Paciente");
        p = ManagerPersona.buscarPersona(ci);
        if (p == null) {
            Main.guiPersona = GuiPersona.get(ci, GuiPersona.GuiRegistrarConsulta);
        }else{
            jLabel7.setText(p.getNombres() + " " + p.getApellidos());
        }
    }//GEN-LAST:event_jLabel7MouseClicked


