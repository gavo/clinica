    public void cargarEspecialidades() {
        especialidades = ManagerEspecialidad.listarEspecialidad();
        jComboBox1.removeAllItems();
        for (Especialidad e : especialidades) {
            jComboBox1.addItem(e.getNombre());
        }
    }

    public void cargarEspecialistas() {
        especialistas = ManagerEspUs.listarUsuarios(especialidades.get(jComboBox1.getSelectedIndex()));
        jComboBox2.removeAllItems();
        for (Usuario u : especialistas) {
            jComboBox2.addItem(u.getPersona().getNombres() + " " + u.getPersona().getApellidos());
        }
    }

