    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Usuario u = ManagerUsuario.buscarUsuario(JOptionPane.showInputDialog("Indique el Nombre del Usuario que desea Agregar"));
        if(u!=null){
            ManagerEspUs.registrarEspUs(new EspUs(especialidades.get(jComboBox1.getSelectedIndex()), u));
        }else{
            JOptionPane.showMessageDialog(this, "Debe Escribir Un Nombre de Usuario Valido","Error Al Seleccionar Usuario",JOptionPane.ERROR_MESSAGE);
            ManagerArchivo.escribirLog("Error: Se intento Registrar una Especialidad a Un Usuario No Valido");
        }
        CargarTabla();
    }//GEN-LAST:event_jButton4ActionPerformed

