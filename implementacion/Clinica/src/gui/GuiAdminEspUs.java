    public static void CargarLista() {
        especialidades = ManagerEspecialidad.listarEspecialidad();
        jComboBox1.removeAllItems();
        for (Especialidad e : especialidades) {
            jComboBox1.addItem(e.getNombre());
        }
    }

    public static void CargarTabla() {
        modelo.setRowCount(0);
        modelo.setColumnCount(0);
        modelo.addColumn("Usuario");
        modelo.addColumn("Persona");
        modelo.addColumn("Estado");
        usuarios = ManagerEspUs.listarEspUs(especialidades.get(jComboBox1.getSelectedIndex()));
        for (EspUs eu : usuarios) {
            modelo.addRow(ManagerEspUs.listarEspUsUsuario(eu));
        }
    }

    public static GuiAdminEspUs get() {
        if (myInstance == null) {
            myInstance = new GuiAdminEspUs();
        }
        return myInstance;
    }

