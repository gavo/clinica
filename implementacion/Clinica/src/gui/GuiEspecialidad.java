    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        puntero = lista.get(jComboBox1.getSelectedIndex());
        jTextField1.setText(puntero.getNombre());
        jTextField2.setText(puntero.getCosto() + "");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (puntero == null) {
            puntero = new Especialidad(jTextField1.getText(),Float.parseFloat(jTextField2.getText()));
            ManagerEspecialidad.registrarEspecialidad(puntero);
        }else{
            puntero.setNombre(jTextField1.getText());
            puntero.setCosto(Float.parseFloat(jTextField2.getText()));
            ManagerEspecialidad.actualizarEspecialidad(puntero);
        }
        puntero = null;
        jTextField1.setText("");
        jTextField2.setText("");
        cargarLista();
    }//GEN-LAST:event_jButton1ActionPerformed


