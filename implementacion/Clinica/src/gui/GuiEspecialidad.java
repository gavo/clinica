    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean isNull = false;
        if (e == null) {
            isNull = true;
            e = new Especialidad();
        }
        e.setNombre(jTextField1.getText());
        e.setCosto(Float.parseFloat(jTextField2.getText()));
        if (isNull) {
            ManagerEspecialidad.registrarEspecialidad(e);
        } else {
            ManagerEspecialidad.actualizarEspecialidad(e);
        }
        GuiAdminEspUs.CargarLista();
        myInstance = null;
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

