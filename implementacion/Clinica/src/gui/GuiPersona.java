    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.p == null) {
            this.p = new Persona(
                    jTextField1.getText().toUpperCase(),
                    jTextField2.getText().toUpperCase(),
                    jTextField3.getText().toUpperCase(),
                    jTextField4.getText().toUpperCase(),
                    jTextField5.getText().toUpperCase()
            );
            ManagerPersona.insertarPersona(p);
        } else {
            this.p.setNombres(jTextField2.getText().toUpperCase());
            this.p.setApellidos(jTextField3.getText().toUpperCase());
            this.p.setDireccion(jTextField4.getText().toUpperCase());
            this.p.setTelefono(jTextField5.getText().toUpperCase());
            ManagerPersona.actualizarPersona(this.p);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


