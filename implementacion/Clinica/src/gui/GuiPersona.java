    private GuiPersona(Persona p) {
        super("Registrando Persona");
        initComponents();
        this.setVisible(true);
        this.setLocationRelativeTo(null);
        this.p = p;
        if (p != null) {
            jTextField1.setText(p.getCi());
            jTextField1.disable();
            jTextField2.setText(p.getNombres());
            jTextField3.setText(p.getApellidos());
            jTextField4.setText(p.getDireccion());
            jTextField5.setText(p.getTelefono());
            jButton1.setText("Modificar Datos Persona");
            this.setTitle("Modificando datos de Persona");
        } else {
            jTextField1.enable();
            jButton1.setText("Registrar Datos Persona");
        }
    }

    public static GuiPersona get() {
        if (myInstance == null) {
            myInstance = new GuiPersona();
        }
        return myInstance;
    }

    public static GuiPersona get(Persona p) {
        myInstance = new GuiPersona(p);
        return myInstance;
    }

